class TestNeo23x0RulesFile(unittest.TestCase):
    """验证 40-neo23x0.rules 文件的结构和完整性"""

    def test_rules_file_exists(self):
        """规则文件应存在"""
        self.assertTrue(
            NEO23X0_RULES_FILE.exists(),
            f"Neo23x0 规则文件不存在: {NEO23X0_RULES_FILE}"
        )

    def test_rules_file_not_empty(self):
        """规则文件不应为空"""
        content = NEO23X0_RULES_FILE.read_text()
        self.assertGreater(len(content.strip()), 0, "规则文件不应为空")

    def test_rules_file_has_attribution_header(self):
        """规则文件应包含来源说明注释"""
        content = NEO23X0_RULES_FILE.read_text()
        self.assertIn('Neo23x0', content, "规则文件应包含 Neo23x0 来源说明")
        self.assertIn('https://github.com/Neo23x0/auditd', content,
                      "规则文件应包含项目 URL")

    def test_rules_file_no_reset_flag(self):
        """-D（清除规则）不应出现在此文件，避免与 00-base.rules 冲突"""
        lines = NEO23X0_RULES_FILE.read_text().splitlines()
        active_lines = [l.strip() for l in lines
                        if l.strip() and not l.strip().startswith('#')]
        self.assertNotIn('-D', active_lines,
                         "-D 不应出现在 40-neo23x0.rules 中（已在 00-base.rules 配置）")

    def test_rules_file_no_buffer_config(self):
        """-b（缓冲区大小）不应出现在此文件，避免与 00-base.rules 冲突"""
        lines = NEO23X0_RULES_FILE.read_text().splitlines()
        active_lines = [l.strip() for l in lines
                        if l.strip() and not l.strip().startswith('#')]
        buffer_lines = [l for l in active_lines if l.startswith('-b ')]
        self.assertEqual(len(buffer_lines), 0,
                         "-b 不应出现在 40-neo23x0.rules 中（已在 00-base.rules 配置）")

    def test_rules_file_has_ignore_flag(self):
        """-i（忽略错误）应存在，确保可选路径不阻断加载"""
        content = NEO23X0_RULES_FILE.read_text()
        self.assertIn('-i', content, "-i 标志应存在（允许可选路径）")

    def test_rules_file_contains_key_neo23x0_rules(self):
        """规则文件应包含 neo23x0 的核心规则键"""
        content = NEO23X0_RULES_FILE.read_text()
        essential_keys = [
            'process_creation', 'perm_mod', 'unauthedfileaccess',
            'tracing', 'anon_file_create', 'bpf', 'namespaces',
            'modules', 'network_modifications',
        ]
        for key in essential_keys:
            self.assertIn(key, content,
                          f"规则文件应包含规则键: {key}")

    def test_rules_file_syntax_valid_lines(self):
        """规则文件中的规则行应具有合法的 auditd 语法"""
        lines = NEO23X0_RULES_FILE.read_text().splitlines()
        valid_prefixes = ('-a ', '-w ', '-A ', '-d ', '-D', '-b ', '-f ',
                          '-e ', '-i', '-r ', '-l ')
        for line in lines:
            stripped = line.strip()
            if not stripped or stripped.startswith('#'):
                continue
            self.assertTrue(
                any(stripped.startswith(p) for p in valid_prefixes),
                f"规则行格式不合法: {stripped!r}"
            )

    def test_rules_file_sorted_after_existing(self):
        """40-neo23x0.rules 应在文件系统排序中位于 30-file.rules 之后"""
        rules_files = sorted(AUDIT_RULES_DIR.glob('*.rules'))
        names = [f.name for f in rules_files]
        self.assertIn('40-neo23x0.rules', names)
        idx = names.index('40-neo23x0.rules')
        self.assertGreater(idx, names.index('30-file.rules'),
                           "40-neo23x0.rules 应排在 30-file.rules 之后")
        self.assertLess(idx, names.index('99-finalize.rules'),
                        "40-neo23x0.rules 应排在 99-finalize.rules 之前")

    def test_all_rules_files_present(self):
        """所有预期的规则文件都应存在"""
        expected_files = [
            '00-base.rules', '10-process.rules', '20-network.rules',
            '30-file.rules', '40-neo23x0.rules', '99-finalize.rules',
        ]
        for fname in expected_files:
            self.assertTrue(
                (AUDIT_RULES_DIR / fname).exists(),
                f"规则文件应存在: {fname}"
            )


