        neo23x0_content = NEO23X0_RULES_FILE.read_text()
        existing_files = [
            '00-base.rules', '10-process.rules',
            '20-network.rules', '30-file.rules',
        ]
        existing_content = ""
        for fname in existing_files:
            existing_content += (AUDIT_RULES_DIR / fname).read_text()

        # neo23x0 特有的键不应出现在原有规则中
        neo23x0_unique_keys = [
            'perm_mod', 'unauthedfileaccess', 'tracing', 'anon_file_create',
            'T1070_006_timestomp', 'bpf', 'namespaces', 'process_vm',
            'power_abuse', '32bit_abi', 'process_creation',
        ]
        for key in neo23x0_unique_keys:
            self.assertNotIn(
                f'key="{key}"', existing_content,
                f"键 '{key}' 是 neo23x0 特有键，不应出现在原有规则文件中"
            )
            self.assertIn(
                key, neo23x0_content,
                f"键 '{key}' 应出现在 40-neo23x0.rules 中"
            )

